public class right_angle_triangle {
    public static void main(String[] args) {
        int a = 4;
        int b = 3;
        int c = 5;
        if ((a*a+b*b)==c*c) {
            System.out.print("yes it is a right angle triangle ");
        }
        else{
            System.out.print("no it is not a right angle triangle ");
        }
    }

}
